package com.makas.model;

public class Car {
	private String brand;
	private int millage;

	public String getBrand() {
		return brand;
	}

	public int getMillage() {
		return millage;
	}

	public void setBrand(String brand) {
		this.brand = brand;
	}

	public void setMillage(int millage) {
		this.millage = millage;
	}
}
